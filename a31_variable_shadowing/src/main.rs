fn main() {
    println!("Hello, world!");

    let some_data = "Hello";
    println!("{}", some_data);
    // output => Hello

    let some_data = 12;
    println!("{}", some_data);
    // output => 12

    let some_data = "Rust!";
    println!("{}", some_data);
    // output => Rust!

    let mut some_data = false;
    some_data = true;
    println!("{}", some_data);
    // output => true

    let some_data = 3.14;
    println!("{}", some_data);
    // output => 3.14
}
